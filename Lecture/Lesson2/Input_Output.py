# Можем выводить данные через ,
# Использование разделителей слов sep = ""
# Использование разделителей строк end = "" 

print ("Hello world! My name is Artem. I'm 22")

name = "Artem"
age = 22

# Вывод строки с переменными
print (name, age)

# Разделители между словами
print("SEP = ''")
print (name, age, sep=" / ")

# Перенос строки. '/n' - перенос строки 
print ("---------->")
print ("END = ''")
print (name, end = ";")
print (age, end = ";")
print ("end", end=";")
